import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import solc from 'solc'

const source = readFileSync('contracts/NexusLaunchToken.sol', 'utf8')
const output = JSON.parse(solc.compile(JSON.stringify({
  language: 'Solidity',
  sources: { 'NexusLaunchToken.sol': { content: source } },
  settings: { optimizer: { enabled: true, runs: 200 }, outputSelection: { '*': { '*': ['abi', 'evm.bytecode.object'] } } },
})))

const errors = (output.errors || []).filter(item => item.severity === 'error')
if (errors.length) throw new Error(errors.map(item => item.formattedMessage).join('\n'))

const contract = output.contracts['NexusLaunchToken.sol'].NexusLaunchToken
mkdirSync('src/contracts', { recursive: true })
writeFileSync('src/contracts/nexusLaunchToken.ts', `// Generated by scripts/compile-launch-token.mjs\nexport const nexusLaunchTokenAbi = ${JSON.stringify(contract.abi, null, 2)} as const\nexport const nexusLaunchTokenBytecode = '0x${contract.evm.bytecode.object}' as const\n`)
